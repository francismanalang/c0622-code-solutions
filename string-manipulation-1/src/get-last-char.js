// define the function
// take the length of the string - 1 and assign it to the variable lastIndex
// take the lastIndex variable and use it to get the last charatcer of the string parameter
// and assign it to the variable lastChar
// return lastChar from the function

function getLastChar(string) {
  var lastIndex = string.length - 1;
  var lastChar = string[lastIndex];
  return lastChar;
}
